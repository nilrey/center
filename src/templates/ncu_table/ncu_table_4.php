<p><u>Пояснения к форме НЦУ/4:</u></p>
<p>1. В столбцах 3-6 таблицы указывается количество детей военнослужащих, состоящих в очереди на получение мест в дошкольные образовательные организации по состоянию на начало отчетного квартала.</p>
<p>2. В столбце 7 таблицы указывается количество обеспеченных детей военнослужащих в текущем квартале (году), с нарастающим итогом к концу года.</p>
<p>3. В столбце 8 таблицы указывается количество обеспеченных детей военнослужащих в предыдущем году.</p>
<p>4. В столбце 9 таблицы указываются проблемные вопросы, требующие совместного решения.</p>